#import math modual for mathemetical function
import math
#user input
data = int(input("Enter number: "))

#square root
squre_root = math.sqrt(data)
#Natural logarithm (log base e) of the number
log = math.log(data)
#Sine of the number (in radians)
sine = math.sin(data)
#print result.
print("Square root: ",squre_root)
print("Logarithm: ", log)
print("sine: ",sine)